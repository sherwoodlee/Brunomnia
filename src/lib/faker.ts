export const fakerFunctionNames = Object.freeze(Object.keys(fakerFunctions));

export const renderFakerValue = async (name: string) => {
  const render = fakerFunctions[name];
  if (!render) throw new Error(`Faker variable '${name}' is not supported.`);
  const { faker } = await import('@faker-js/faker');
  return String(render(faker));
};
