import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'esbuild';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const packageRoot = path.join(root, 'scripts', 'plugin-vendored');
const outputPath = path.join(root, 'src', 'lib', 'pluginVendored.generated.ts');
const libraries = [
  { name: 'uuid', entry: "module.exports = require('uuid');" },
  { name: 'ajv', entry: "module.exports = require('ajv').default || require('ajv');" },
];

const generated = [];
for (const library of libraries) {
  const packagePath = path.join(packageRoot, 'node_modules', library.name, 'package.json');
  const packageJson = JSON.parse(fs.readFileSync(packagePath, 'utf8'));
  const result = await build({
    stdin: { contents: library.entry, resolveDir: packageRoot, loader: 'js' },
    bundle: true,
    format: 'cjs',
    platform: 'browser',
    target: 'es2020',
    define: { 'process.env.NODE_ENV': '"production"' },
    legalComments: 'none',
    minify: true,
    write: false,
  });
  const bundle = result.outputFiles[0].text;
  const factory = ['function () {', '  var module = { exports: {} };', '  var exports = module.exports;', '  (function (module, exports) {', bundle, '  })(module, exports);', '  return module.exports;', '}'].join('\n');
  generated.push({ name: library.name, version: packageJson.version, factory });
}

const lines = [
  '// Generated by scripts/generate-plugin-vendored.mjs.',
  ...generated.flatMap(({ name, version, factory }) => {
    const constant = name.toUpperCase();
    return [
      `export const ${constant}_PLUGIN_MODULE_VERSION = ${JSON.stringify(version)};`,
      `export const ${constant}_PLUGIN_MODULE_FACTORY = ${JSON.stringify(factory)};`,
    ];
  }),
  '',
];
fs.writeFileSync(outputPath, lines.join('\n'));
